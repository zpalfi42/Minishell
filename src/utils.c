#include "minishell.h"

int	ft_strcmp(char *s, char *d)
{
	int i;

	i = 0;
	while (s[i] != '\0' && d[i] != '\0')
	{
		if (s[i] != d[i])
			return (0);
		i++;
	}
	if (s[i] == d[i] && s[i] == '\0')
		return (1);
	return (0);
}